/**
 * @param {string} path
 * @returns {Boolean}
 */
export function isExternal(path) {
  return /^(https?:|mailto:|tel:)/.test(path)
}

/**
 * @param {string} str
 * @returns {Boolean}
 */
export function validPhone(str) {
  const phoneRegex = /^1[3456789]\d{9}$/
  return phoneRegex.test(str)
}
